// Declaring a Variable
var age;
let name;
const pi = 3.14;

//In this step, you're essentially reserving a spot in memory for the variable but not yet giving it a value.

// Assigning a Variable:
age = 25;
name = "John";

//At this point, you're giving the variable a value that it will hold in memory.

//You can also combine declaration and assignment into a single step:

var age = 25; // Declaring and assigning 'age' in one step
let name1 = "John"; // Declaring and assigning 'name' in one step
